/**
 * Constants for vector visualization scales.
 * These determine how many pixels represent one unit of physical quantity.
 */

/**
 * Scale for velocity vectors (pixels per m/s)
 */
export const VELOCITY_VECTOR_SCALE = 50;

/**
 * Scale for force vectors (pixels per Newton)
 */
export const FORCE_VECTOR_SCALE = 10;

/**
 * Scale for acceleration vectors (pixels per m/s²)
 */
export const ACCELERATION_VECTOR_SCALE = 20;

/**
 * Minimum magnitude for velocity vector display (m/s)
 */
export const VELOCITY_MIN_MAGNITUDE = 0.05;

/**
 * Minimum magnitude for force vector display (N)
 */
export const FORCE_MIN_MAGNITUDE = 0.1;

/**
 * Minimum magnitude for acceleration vector display (m/s²)
 */
export const ACCELERATION_MIN_MAGNITUDE = 0.1;
